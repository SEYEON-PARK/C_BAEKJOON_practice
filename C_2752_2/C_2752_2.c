/*
동규는 세수를 하다가 정렬이 하고싶어졌다.
숫자 세 개를 생각한 뒤에, 이를 오름차순으로 정렬하고 싶어 졌다.
숫자 세 개가 주어졌을 때, 가장 작은 수, 그 다음 수, 가장 큰 수를 출력하는 프로그램을 작성하시오.

숫자 세 개가 주어진다. 이 숫자는 1보다 크거나 같고, 1,000,000보다 작거나 같다. 이 숫자는 모두 다르다.

제일 작은 수, 그 다음 수, 제일 큰 수를 차례대로 출력한다.
*/

#include <stdio.h>

int main(void)
{
    int a, b, c; // 변수 선언
    scanf("%d %d %d", &a, &b, &c); // 사용자로부터 세 개의 정수 입력받기
    for (int i = 0; i < 3; i++) // i가 0부터 3이 되기 전까지 1씩 증가하며 반복
    {
        printf("%d ", (a < b) ? ((a < c) ? a : c) : ((b < c) ? b : c)); // a, b, c 중 가장 작은 수를 출력하기
        if (((a < b) ? ((a < c) ? a : c) : ((b < c) ? b : c)) == a) // 만약, 가장 작은 수가 a라면
            a = 1000001; // a에 1000001 대입하기
        else if (((a < b) ? ((a < c) ? a : c) : ((b < c) ? b : c)) == b) // 만약, 가장 작은 수가 a가 아니라 b라면
            b = 1000001; // b에 1000001 대입하기
        else // 가장 작은 수가 a도 아니고 b도 아니라면
            c = 1000001; // c에 1000001 대입하기
    }
    return 0;
}
